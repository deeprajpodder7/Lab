/*Write a Java program that takes five numbers as input to calculate and print 
the average of the numbers.*/


class Avg{
public static void main(String args[]){
int a=10;
int b=20;
int c=30;
int d=40;
int e=50;
int avg= (a+b+c+d+e)/5; //to store the average of all these numbers
System.out.println("The first number is :" + a);
System.out.println("The second number is :" + b);
System.out.println("The third number is :" + c);
System.out.println("The fourth number is :" + d);
System.out.println("The fifth number is :" + e);
System.out.println("The average of these numbers is :" + avg); //the output of average
}
}
